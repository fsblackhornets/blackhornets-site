function applyAdminTranslations(lang) {
	const t = adminTranslations[lang] || adminTranslations.en;
	document.querySelectorAll("[data-i18n]").forEach((el) => {
		const key = el.getAttribute("data-i18n");
		if (t[key] !== undefined) {
			el.textContent = t[key];
		}
	});

	document.querySelectorAll("[data-i18n-title]").forEach((el) => {
		const key = el.getAttribute("data-i18n-title");
		if (t[key] !== undefined) {
			el.title = t[key];
		}
	});

	document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
		const key = el.getAttribute("data-i18n-placeholder");
		if (t[key] !== undefined) {
			el.placeholder = t[key];
		}
	});

	document.querySelectorAll("[data-i18n-label]").forEach((el) => {
		const key = el.getAttribute("data-i18n-label");
		if (t[key] !== undefined) {
			el.label = t[key];
		}
	});

	// Update lang toggle button states
	document.querySelectorAll(".lang-btn").forEach((btn) => {
		btn.classList.toggle("active", btn.dataset.lang === lang);
	});
}

function changeAdminLanguage(lang) {
	localStorage.setItem("language", lang);
	applyAdminTranslations(lang);
}

function initAdminLanguage() {
	const lang = localStorage.getItem("language") || "en";
	applyAdminTranslations(lang);
}

document.addEventListener("DOMContentLoaded", initAdminLanguage);
